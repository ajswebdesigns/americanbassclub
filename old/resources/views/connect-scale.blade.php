        <p><b>My Scale will still not pair with my Android device, I’ve tried previous steps, what do I do now?</b> Go
          to Bluetooth Settings - Disable bluetooth by Sliding off. Then wait 3 seconds and enable again by sliding on.
          This should reset the device bluetooth setting to enable you to pair scale through the app.</p>

        <p><b>My scale shows it is connected to bluetooth but it will not transfer weight to the app?</b> Make sure you
          do not have power save enabled on your mobile device. Depending on the setting your bluetooth will connect and
          then unpair to device if power save is enabled. </p>

        <p><b>My scale will not lock in on weight how do I get it to lock?</b> Make sure you have the Auto Hold feature
          enabled on the scale. To turn hold on/off you tap and hold the TARE button for 3 seconds. ON indicates the
          auto hold feature is enabled and OF indicates the auto hold feature is disabled. The ConnectScale 3 is a
          hanging scale, if you are on an unstable boat with rough water conditions and can not hold the scale steady it
          may not lock in.</p>

        <p><b>Will the app work in areas without data coverage?</b> The app is designed for the core features to
          function when in areas without coverage. However you must already be logged into the app and have it open in
          the background before entering an area without data coverage. The Scale works with bluetooth and will transmit
          data to the app without data coverage as long as the scale is paired to the device and app. One note when
          adding photos to a catch in an area with no service or limited/slow data coverage, please take photos of your
          catch with the native camera app on your device and upload photos to your catches in the ConnectScale app when
          you return to a good internet signal</p>
