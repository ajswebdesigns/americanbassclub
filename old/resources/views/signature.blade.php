<b>PAYMENT SCHEDULE:</b> Team entry fees are $2000 per tournament. Deadline is 30
days prior to the first day of the event. Teams must pay entry fee in full to be placed in
the tournament. Time of entry into the tournament is based on payment in full for the
tournament. Tournaments are only for ABC members in good standing and are first
come first serve.                    </p>


