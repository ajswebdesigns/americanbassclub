                    <p>
<b>American Bass Club (ABC)</b> Rules for the 2022 season. ABC was established for
fishermen by fishermen. ABC is all about conservation of our sport and promoting
fishing for our youth and the hard working American. We support our Veterans and
most of all we love the sport of fishing. ABC's number one rule is to have fun and
respect others. ABC reserves the right to make needed changes to these rules in order
to ensure safety and welfare of the ABC anglers, and or the public and to preserve the
integrity of ABC. Any rule changes will immediately supersede all previously published
copies of the rules. It is your responsibility to read and fully understand the rules and
how you are effected by not complying with the following rules. Every member will be
required to sign an affidavit that he/she has read, understands and accepts all ABC
rules. All ABC tournaments are team events with all awards going to the team. Anglers
may not fish as individuals. A team consist of two(2) anglers, one of which must be
designated as the "captain". The team that qualifies for the national championship must
fish the national championship. Alternates cannot fish the championship. Only in the
event of military deployment or a death of a partner, the tournament director may, after
review, approve an alternate partner for the national championship. The designated
captain will receive all tax documents at the end of each tournament year. All members
must have a valid fishing license and are required to obey all federal, state and local
laws and regulations.                     </p>
                    <p>
<b>INTERPRETATION & ENFORCEMENT •</b> Interpretation of these rules will be left
exclusively to the tournament director. In the event of a rule violation, the tournament
director may impose penalties such as weight loss, disqualification, forfeiture of prizes
and or fines not less than $300, no more than $5000 and ineligibility to participate in any
ABC club event. Decisions of the tournament director are final in all matters and are not
subject to appeal and are not reviewable by any court of law. Alleged rule violations
must be reported to the tournament director immediately. The protestor also agrees with
regards to verifying the allegation, he or she may be subject to polygraph exam and
agree to testify in a court of law if necessary.                     </p>
                    <p>
<b>PAYBACK & PARTICIPATION •</b> Payback is based on the number of paid entries for
each tournament. Participation is open only to ABC members who are 16 years of age
or older. Any person under the age of 18 (19 in Alabama and Nebraska) entering a
tournament must be paired with a partner the age of 18 (19 in Alabama and Nebraska)
and have the signature of a parent or legal guardian in the provided space on the entry
form. Any person’s participation in any tournament is at the sole discretion of ABC
officials. Any person who, in the judgment of the tournament director, is disabled or
handicapped in such a manner as to unfairly hinder his or her captain or angler partner
or compromise his or her safety or the safety of other competitors or the boating public
may not be eligible to participate. All members must have a valid fishing license for the
waters they fish. Random checks will be conducted at each tournament. Failure to
provide proof of a valid fishing license within a reasonable amount of time when
requested by the tournament director will result in disqualification for that day and any
preceding days in which the license was required for the tournament. ABC is designed
for the weekend angler and not for Professional level fishermen. A Professional is
determined by level of fishing that he or she participates in. If in the past two years you
have fished in the Tackle Warehouse Pro Circuit, MLF Cup Events, Red Crest, Bass Pro
Tour or the Bass Master Elite Series you are considered a Professional and do not 
qualify to fish ABC events. ABC reserves the right to refuse membership to any and all
events to any individual or team at the discretion of the tournament director.                    </p>
                    <p>
<b>TEAM ENTRY:</b> The top 40 Teams from each regional qualifier of this calendar year will
receive priority entry into the national championship scheduled the next calendar
year. Top 5 teams from each regional qualifier of this calendar year will receive free
entry into the national championship scheduled the next calendar year. You must fish a
regional event to qualify for the nation championship. All regional entry priority will be
assigned by the date and time of paid entry. Entry priority will be final once full payment
is received. Entries may be made either by mail, phone or on the Internet at
americanbassclub.com. Do not make entry by more than one method. Published
payouts for each tournament are based on 200 Team entries. ABC may, in its sole
discretion, accept entries after the entry/balance deadline for any tournament to even
the field. ENTRY FEES ARE NONTRANSFERABLE AND NONREFUNDABLE ONCE
ACCEPTED INTO A TOURNAMENT. Entries received after a tournament has filled will
be placed on a waiting list. Those teams placed on the waiting list and not accepted into
the tournament will receive a full refund after the tournament. CONFIRMED ENTRIES
ARE CONTINGENT UPON COMPLETE ENTRY FEE BEING PAID BY THE ENTRY/
BALANCE DEADLINE DATE. Entry fees may be transferred or refunded if ABC is    notified prior to the entry/balance deadline or event being sold out. <b>ENTRY FEES WILL
NOT BE TRANSFERRED OR REFUNDED AFTER THE ENTRY/BALANCE
DEADLINE.</b> Any entry balances received after the entry balance deadline (except those
called up from the waiting list) will be charged a $50 late fee.                 </p>
