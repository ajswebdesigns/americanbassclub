<p><strong>PURCHASING OF INFORMATION •</strong> The purchasing of, or bartering for, information about 
locating or catching fish on tournament waters, including but not limited to GPS waypoints 
(other than through commercially available sources) and the hiring of fishing guide services is 
not permitted 6 months prior to each tournament. The sharing of tournament winnings with 
nonparticipants in exchange for information about locating or catching fish on tournament waters 
is strictly prohibited and will be cause for disqualification from the entire tournament. The 
purchasing of, or bartering for, fishing locations from another contestant is strictly prohibited and 
will be cause for both contestants to be disqualified from the entire tournament.</p>  
<p><strong>OFF-LIMITS, PRACTICE & COMPETITION •</strong> The official practice period is 6 weeks from 
the first day of the tournament at all events. Contestants may not enter tournament waters to 
locate bass or potential fishing waters or sightsee after 5 PM on Wednesday prior to the 
tournament. Testing equipment on tournament waters after the start of the off-limits period is 
permitted only with prior approval from the tournament director. On days 1, 2 and 3 participants 
may NOT use the internet to look at the live leader board or to obtain any information pertaining 
to fishing during the tournament hours. On days 1, 2 and 3, members may not solicit and/or 
receive information about locating or catching fish on tournament waters from anyone except 
other anglers confirmed in the tournament and through publicly available sources (quasi-public 
websites, blogs and/or social media pages, including but not limited to Facebook, Instagram, 
Snapchat and Periscope, set up for the specific purpose of sharing information with individuals 
or a small group of individuals are NOT publicly available sources). During competition days 
and practice, members may not place brush or other physical fish attractors, including chum of 
any kind, in tournament waters. During competition days and practice, members may not follow 
a nonparticipants boat or participate in the placing of markers, brush or other fish attractors by 
nonparticipants. The practice of “hole sitting” by anyone is not permitted. Anyone that is not 
entered in the ABC event is a nonparticipant. No practice is permitted at night (beginning at 
sunset and ending at sunrise off-limits) during the event and official practice period. This 
includes running to and from a fishing location. Members may only practice with ABC members 
or may practice alone during the official practice period. Participants may practice with any 
member of ABC provided the member has also observed the off-limits period and procedures 
and that member's dues are current and up to date. Members may also practice with an approved 
media representative. Members may fish with nonmembers during the practice period if they are 
registered in a fishing event outside of ABC during that event only. Flights over tournament 
waters, including drones, for the express purpose of locating fish or fishing locations are not 
permitted. Underwater cameras are not permitted during official tournament days. </p>
<p><strong>REGISTRATION & PRETOURNAMENT MEETING •</strong> Each member's annual dues must be 
current the Wednesday prior to each event. Each member must login and watch the 
pretournament video located online on the ABC website for the tournament 24 hours prior to the 
Event. All members must watch the pretournament video to record flights, takeoff time, check-in 
time, off-limits areas, size and creel limits, and any other relevant tournament rule information. If 
any members fail to watch the entire pretournament video, they may be disqualified from the 
event. </p>
<p><strong>SAFETY •</strong> Safe boating must be observed at all times. Each contestant is required to wear a 
fastened U.S. Coast Guard-approved personal flotation device anytime the combustion engine is 
running from boat check until weigh-in each day of the tournament. All boats must have a Non-
Inflatable U.S. Coast Guard-approved personal flotation device for all persons on board. All 
boats must be equipped with a factory-installed emergency ignition-shutoff device that must be 
securely attached to the driver’s body with a standard-length, factory tether whenever the 
combustion engine is running. Anglers may fish and charge batteries using an onboard charger 
by letting the combustion engine idle in neutral only if everyone in the boat is wearing a fastened 
U.S. Coast Guard-approved personal flotation device. All persons must be seated, and a driver 
must be behind the steering wheel in full control of the boat whenever it is on plane, or the 
combustion engine is running and in gear. At the discretion of the tournament director, 
tournament days may be shortened, postponed or canceled due to unsafe weather or water 
conditions. Competitors may leave their boat and seek shelter in bad weather or when danger is 
imminent. Contestants involved in boating accidents and/or safety incidents may be required to 
complete a Boatus.org boating safety course before participating in another ABC tournament. A 
free course for each state is available at boatus.org.  </p>
<p><strong>SPORTSMANSHIP •</strong> All contestants are required to follow high standards of sportsmanship, 
courtesy and conservation and to conduct themselves in a manner that will be a credit to 
themselves, to ABC, ABC's sponsors, the sport of fishing and ABC's efforts to promote the sport. 
Conduct not complying with these standards includes, but is not limited to, the following: </p>
<ul>
  <li>Violation of, or failure to comply with, any of the ABC rules. </li>
  <li>Consumption and/or possession of alcoholic beverages or any kind of mind-altering 
    substance during registration or tournament hours extending through the weigh-in 
    procedure.</li>
    <li>Abuse of, or addiction to, mind-altering substances.</li>
    <li>Conviction of a felony within the past 24 months.</li>
    <li>Suspension/disqualification, probation or ban from any tournament or fishing 
      organization. </li>
      <li>Boat operation in an unsafe or unsportsmanlike manner. </li>
      <li>Violation of a posted no-wake area. </li>
      <li>Publicly criticizing or questioning the integrity of ABC or ABC officials, including but 
        not limited to ABC rules, payouts, policies and procedures. </li>
        <li>Dissemination of disparaging content or content that one should reasonably know would 
          harm the reputation of ABC, ABC officials or ABC sponsors. </li>
          <li>Any other words, conduct or actions reflecting unfavorably upon efforts to promote 
            safety, sportsmanship, fair competition and compliance with tournament rules.</li>
</ul>

<p>In case of any conduct not complying with the standards outlined above, ABC shall have the 
right to refuse an application to compete, to deny a confirmed application to compete, to 
disqualify a contestant or impose any penalty outlined in these rules. In case of disqualification 
angler will be responsible for the total due per the angler agreement in full. </p>
<p><strong>BOAT OPERATION •</strong> Participants may NOT use mobile communication devices, including but 
not limited to, cell phones, tablets, marine radios, walkie-talkies, CBs, etc., to look at the live 
leader board, internet or to communicate with anyone about locating or catching fish on 
tournament waters during tournament hours. Mobile communication devices may be used during 
tournament hours to communicate with lockmasters about locking, to communicate with the 
tournament director or to communicate with ABC editors or producers. Participants may use 
smart phones and/or tablets during tournament hours for GPS, mapping, weather, power 
generation, barge traffic updates, tidal charts, personal fishing notes and related data. At no time 
during tournament hours may participants read or discuss social media posts from other 
participants. Participants are expected to compete every day for which they are entered or qualify 
for; failure to do so may result in ineligibility to compete in future ABC tournaments. If a special 
circumstance arises that inhibits your ability to compete, the tournament director may allow you 
to withdraw from competition and maintain the weight caught to that point. Depending on the 
need to maintain an equal number of participants the next highest angler in the tournament 
standings may be invited to continue competition.  </p> 
   
<p><strong>BOAT & HORSEPOWER REGULATION •</strong> All boats must be propeller-driven, a minimum 
of 16 feet in length and have a rear deck. All boats must be equipped with wheel steering; no 
other steering device will be permitted. No barges or similarly cumbersome craft will be 
permitted. Each boat must have all required U.S. Coast Guard safety equipment. Boats must 
contain a properly aerated live well space to maintain alive a limit catch of bass. Maximum 
horsepower for all outboards used in tournament competition will be 250 horsepower, not to 
exceed the horsepower capacity set forth on the “Maximum Capacities” placard described below. 
Each boat must have a clearly legible “Maximum Capacities” (or comparably titled) placard that 
includes a maximum horsepower rating affixed to the boat by the manufacturer. The horsepower 
of the outboard engine must not exceed the maximum horsepower capacity specified on this 
placard or the 250-horsepower maximum set by ABC. By signing the entry form, members agree 
to submit their boat and/or motor to an inspection by ABC personnel. Falsifying information on 
entry forms or altering the horsepower numbers on the motor or rating placard will be cause for 
disqualification from the tournament and may result in ineligibility to compete in future ABC 
tournaments. Fishing platforms must be factory-installed equipment or commercially available 
aftermarket equipment that is permanently installed in the boat. No portable platforms may be 
used in tournament competition. Fuel may be carried only in factory-installed (built-in) fuel 
tanks. Any additional fuel used during the tournament day must be purchased from a retail 
facility open to the public and pumped through a hose with a nozzle. The use of generators, other 
than solar or wind-powered generators, is prohibited in contestant boats during tournament hours 
on all competition days. </p>
<p><strong>PERMITTED FISHING METHODS •</strong> Fishing is defined as having a lure attached to a line 
and a rod and reel with the rod in hand. All bass must be caught alive in a conventional sporting 
manner. All lures must adhere to state regulations. Only artificial lures may be used, with the 
exception of pork trailers and biodegradable soft baits. Alabama rigs and similar umbrella-type 
rigs are NOT allowed. Only ONE fishing rod may be used at a time. Trolling as a method of 
fishing is strictly prohibited. Trolling is defined as operating the combustion engine to extend a 
cast or lengthen a retrieve. All bass caught while sight-fishing must be hooked inside the mouth. 
ABC is committed to conservation and the well-being of the fish. Netting of fish is allowed with 
the use of a rubber netting material only. It is the responsibility of the anglers to make sure the 
fish are well cared for. ABC requires all members to make all attempts to keep fish off the carpet 
and release fish as fast as possible. In the event of catching fish out of deep water or where 
anglers recognize a fish is stressed. It is the responsibility of all anglers to (FIZ) their catch, if 
necessary, prior to release. ABC tournament director may at any time request proof of fizzing 
device from any participant in the tournament. </p>
<p><strong>PERMITTED FISHING • </strong>Members may fish anywhere on tournament waters available to the 
public and accessible by boat except areas posted or otherwise designated as “off-limits,” “no 
boats,” “keep out,” “restricted,” “no trespassing” or “no fishing” (or similar language or 
markings intended to restrict public access) by ABC, commercial property owners and/or local, 
state or federal officials, or within 50 yards of a members boat that was first anchored. An 
anchored boat is a boat held in a stable position by a line attached to a weight or by a Power-Pole 
or similar shallow water anchor with the trolling motor in the up position. All fishing must be 
conducted from the boat. At no time may a member leave the boat to land a fish or to make the 
boat more accessible to fishing waters. The cutting of trees, bushes and/or logs after the start of 
the pretournament meeting and/or the removal of official commercial, local, state or federal 
barricades at any time to make an area more accessible by boat or gain access for fishing is 
strictly prohibited and may result in disqualification from the tournament. The use of cables, 
ropes, chains or any type of block and tackle system to maneuver a boat into fishing waters is 
strictly prohibited and will result in disqualification from the tournament. Participants must leave 
and return to the check-in by boat. The boat must remain in the tournament waters during the 
tournament day. No tournament boat may be loaded on a trailer before the check-in time except 
with the permission of the tournament director. In the event of equipment failure or emergency 
the tournament director must be notified as soon as it is safe to do so. There are then two 
permitted methods of returning to the check-in: (1) by member’s remaining in their boat and 
being towed by water, or (2) with permission from the tournament director, by members entering 
the boat of another member. Under these two conditions both participants catches may be 
counted without a penalty (except for late penalties, dead-fish penalties or other penalties 
pertaining to other tournament rules). Members who elect to return to the check-in by any other 
means than cited above will forfeit their day’s catch to that point in time of the tournament day. 
Abandoning a boat and leaving it adrift without proper tie-off or anchoring after a mechanical 
failure may result in disqualification of that day’s weight. Any member returning to the check-in 
point will be eligible to restart and resume competition under the supervision of the tournament 
director or a designated tournament official. It is the sole responsibility of the member to locate 
the tournament director to request a restart. </p>
<p><strong>CHECKPOINTS •</strong> There will be only one checkpoint for boat check in the morning and one 
check-in point in the afternoon. Failure to go through boat check in the morning or failure to 
check in at the check-in point in the afternoon will result in disqualification. All fishing must 
cease upon check-in. </p>
<p><strong>LATE PENALTY • </strong>Participants who are not at the check-in area at the appointed time will be 
penalized 1 pound per minute, deducted from the total weight, including any weight to be 
counted toward the Big Bass Award. Any member more than 15 minutes late will lose credit for 
that day’s weight. In the event of a dead fish participants will be penalized .50 pound deducted 
from the total weight. </p>
<p><strong>LIVE FISH •</strong> ABC is dedicated to conservation of the sport. All fish are to be scaled using the 
American Bass Club digital provided scale system for the live leader board. Every effort must be 
made to keep bass alive through the use of a properly aerated live-well. All anglers are 
responsible for their fish until they are released after the weigh in process. All members in the 
top 30 are required to provide video recordings the entire 8 hours of all days of competition. The 
SD card for each day must be turned in for review at the end of the event. Failure to turn in 
recordings or if recordings are not viewable the anglers day will be disqualified, and all winnings 
will be forfeit. ABC will review all recordings for the top 30 anglers. Any and all winnings are 
subject to be withdrawn / recalled if the recordings are not viewable, didn't record the entire day 
or include the entire catch and weigh process for every catch. In the event there is an issue with 
your scale or recording device you must contact the director immediately for alternate 
instructions. </p>
<p><strong>SCORING •</strong> Scoring is determined by the pounds and ounces of each angler’s catch during each 
tournament. Only largemouth, spotted, redeye or smallmouth bass are accepted species. No 
regurgitated forage or other foreign matter may be placed in a bass. The daily limit will be five 
bass unless conditions dictate a reduced limit for conservation purposes or the state or lake limit 
is less than five, in which the reduced limits will prevail. Bass are considered in possession once 
placed in the boat. The minimum length limit for all species of bass will be 14 inches. If state or 
lake size limit is different the state or lake size limits will prevail. Angler of the year will be 
scored using a point system 1st place would be 150 points, 2nd place would be 149, 3rd place 
would be 148 and so on. Angler at the end of the 5th event with the most points will become the 
angler of the year.  </p>
<p><strong>TIES •</strong> The heaviest one-day catch for the tournament will break ties on day 2. If a tie remains, it 
will be broken first by total number of fish for the tournament and then by biggest fish. If a tie 
remains it will be by lottery. </p>
<p><strong>LOGO DISPLAY & MEDIA COVERAGE •</strong> During all days of competition all anglers are 
required to have the ABC logo at least 3” in diameter displayed on the front of the jersey or shirt.  
Any and all tobacco logos are prohibited and may not be worn or displayed. Anglers may wear 
their own apparel every day. ABC reserves the right, in its sole discretion, to prohibit any 
angler’s apparel that it finds to be offensive. Smoking, vaping or any other smokeless devices are 
not allowed in the weigh-in line during all days of competition. All decisions are at ABC’s sole 
discretion. </p>
<p><strong>POLYGRAPH •</strong> Each member agrees to submit, by signature on the tournament entry form, to a 
polygraph or voice stress analysis examination and to abide by its conclusion. Failure to pass an 
examination will result in disqualification from the tournament and forfeiture of any winnings. 
Members that fail an examination will be fined a minimum of $5000 and will be subject to legal 
ramifications. Truth verification tests will be used at ABC’s sole discretion, and the 
determination of the meaning of the results will be made solely by ABC. If any angler receiving 
winnings, leaves without the tournament director's permission or refuses to be questioned or take 
an examination, he or she will forfeit any and all winnings and be permanently banned from the 
ABC club and events. If video review concludes any member has violated the rules or has 
manipulated a catch in any way that angler will be disqualified and will be banned from the ABC 
club and any events. The member will be fined a minimum of $5000 and will be subject to legal 
ramifications. </p>
 
<p><strong>INSURANCE •</strong> Liability insurance with coverage of not less than $300,000 per occurrence is 
required of all members in any ABC event. Members agree ABC is only hosting and 
coordinating events. Members agree to hold harmless ABC, ABC officials or ABC sponsors. All 
Proof of insurance must be with the boat being used and must cover all passengers in the boat. 
Random checks will be conducted. Failure to provide proof of insurance when requested by the 
tournament director may result in disqualification. All members may be required to submit a 
liability waiver along with the anglers entry form. </p>


        
