        <h2 class="mt-5">Invitational Rules</h2>
        <p><strong> Bass Club (ABC) Fishermans Central Invitational</strong> Rules for the 2023 season. ABC 
was established for fishermen by fishermen. ABC is all about family, conservation of our sport 
and promoting fishing for our youth and the hard-working American. We support our Veterans 
and most of all we love the sport of fishing. ABC's number one rule is to have fun and respect 
others. ABC reserves the right to make needed changes to these rules in order to ensure safety 
and welfare of the ABC anglers, and / or the public and to preserve the integrity of ABC. Any 
rule changes will immediately supersede all previously published copies of the rules. It is your 
responsibility to read and fully understand the rules and how you are affected by not complying 
with the following rules. Every member will be required to sign these rules as an affidavit that 
he/she has read, understands and accepts all ABC rules and regulations. </p> 
<p><strong>INTERPRETATION & ENFORCEMENT •</strong> Interpretation of these rules will be left 
exclusively to the tournament director. In the event of a rule violation, the tournament director 
may impose penalties such as weight loss, disqualification, forfeiture of prizes and / or fines not 
less than $300, no more than $5000 and ineligibility to participate in any ABC club event. 
Decisions of the tournament director are final in all matters and are not subject to appeal and are 
not reviewable by any court of law. Alleged rule violations must be reported to the tournament 
director immediately. The protestor also agrees with regards to verifying the allegation, he or she 
may be subject to polygraph exam and agree to testify in a court of law if necessary.</p> 
<p><strong>PAYBACK & PARTICIPATION •</strong> Payback is based on the number of paid entries for each 
tournament. Participation is by invitation only to ABC members who are 16 years of age or 
older. Any person under the age of 18 (19 in Alabama and Nebraska) entering a tournament must 
have the signature of a parent or legal guardian in the provided space on the parental consent 
form. Any person’s participation in any tournament is at the sole discretion of ABC officials. 
Any person who, in the judgment of the tournament director, is disabled or handicapped in such 
a manner as to unfairly hinder his or her safety or the safety of other competitors or the boating 
public may not be eligible to participate. All members must have a valid fishing license for the 
waters they fish. Random checks will be conducted at each tournament. Failure to provide proof 
of a valid fishing license within a reasonable amount of time when requested by the tournament 
director will result in disqualification for that day and any preceding days in which the license 
was required for the tournament.  ABC reserves the right to refuse membership to any and all 
events to any individual at the discretion of the tournament director. All anglers in the top 30 are 
required to provide video recordings the entire 8 hours of all days in the event. The SD card for 
each day must be turned in for review at the end of the event. Failure to turn in recordings or if 
recordings are not viewable the days weight will be disqualified, and all winnings will be forfeit. 
ABC will review all recordings for the top 30 anglers. Any and all winnings are subject to be 
withdrawn if the recordings are not viewable, didn't record the entire day or include the entire 
catch and weigh process for every catch brought to the scale. All winnings will be awarded by 
check or direct deposit after video review within 72 hours after the final day of the event. </p> 
<p><strong>ANGLER ENTRY:</strong> Entry into the Fishermen’s Central Invitational is for ABC members in 
good standing and by official invitation only. Angler of the year (AOY) will receive free entry 
into the championship and the 2024 Fishermen’s Central Invitational. You must fish all five 
events to qualify for the championship. All entries into the championship are based on AOY 
points. The top 50 in points will advance to the championship. All event entry priority will be 
assigned by the date and time of paid entry. ABC reserves the right to change the schedule as 
need due to schedule conflicts. Entry priority will be final once full payment is received. Entries 
may be made either by mail, phone or on the Internet at americanbassclub.com. Do not make 
entry by more than one method. Published payouts for each tournament are based on 100 entries. 
ABC may, in its sole discretion, accept entries after the entry/balance deadline for any 
tournament to even the field. All deposits are non-refundable and not transferable. ENTRY FEES 
ARE NONTRANSFERABLE AND NONREFUNDABLE ONCE ACCEPTED INTO A 
TOURNAMENT. Entries received after a tournament has filled will be placed on a waiting list. 
Those placed on the waiting list and not accepted into the tournament will receive a full refund 
after the tournament. CONFIRMED ENTRIES ARE CONTINGENT UPON COMPLETE 
ENTRY FEE BEING PAID BY THE ENTRY/BALANCE DEADLINE DATE. Entry fees may 
be transferred if ABC is notified prior to the entry/balance deadline or event being sold out. 
ENTRY FEES WILL NOT BE TRANSFERRED OR REFUNDED AFTER THE 
ENTRY/BALANCE DEADLINE. Failure to pay entry fees will result in late fees, fines and legal 
ramifications. Any entry balances received after the entry balance deadline (except those called 
up from the waiting list) will be charged a $300 late fee.  </p>
