hand. All bass must be caught alive in a conventional sporting manner. All
lures must adhere to state regulations. Only artificial lures may be used, with
the exception of pork trailers and biodegradable soft baits. Alabama rigs and
similar umbrella-type rigs are NOT allowed. Only ONE fishing rod may be used at
a time. Trolling as a method of fishing is strictly prohibited. Trolling is
defined as operating the combustion engine to extend a cast or lengthen a
retrieve. All bass caught while sight-fishing must be hooked inside the
mouth.&nbsp;ABC is committed to conservation and the well-being of the fish.
Netting of fish is allowed with the use of a rubber netting material only. It
is the responsibility of the anglers to make sure the fish are well cared for.
ABC requires all members to make all attempts to keep fish off the carpet and
release fish as fast as possible. In the event of catching fish out of deep
water or where anglers recognize a fish is stressed. It is the responsibility
of all anglers to (FIZ) their catch, if necessary, prior to release. ABC
tournament director may at any time request proof of fizzing device from any
team in the tournament.<o:p></o:p></span></p>

